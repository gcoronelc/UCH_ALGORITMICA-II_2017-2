    private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
        // Datos
        Categoria cat = (Categoria) cboCategoria.getSelectedItem();
        int cantPart = Integer.parseInt(txtCantPart.getText());
        // Proceso
        double[] repo = VentaService.procesarVenta(cat, cantPart);
        // Reporte
        DefaultTableModel tabla;
        tabla = (DefaultTableModel) tblRepo.getModel();
        tabla.setRowCount(0);
        tabla.addRow(new Object[]{"Precio",repo[0]});
        tabla.addRow(new Object[]{"Cantidad",cantPart});
        tabla.addRow(new Object[]{"Importe",repo[1]});
        tabla.addRow(new Object[]{"Impuesto",repo[2]});
        tabla.addRow(new Object[]{"Total",repo[3]});
        tabla.addRow(new Object[]{"Porc. Gana.",repo[4]});
        tabla.addRow(new Object[]{"Ganancia",repo[5]});
    }//GEN-LAST:event_btnProcesarActionPerformed

